#While Schleife - eine Bedingung wird auf seine Wahrheit geprüft, und solange wiedeholt, bis er false ist
counter = 5
while counter < 10:
    print("Hier steht Code, der wiederholt ausgeführt werden soll")
    counter += 1        #counter wird mit jeder Wiederholung um 1 erhöht, bis die Bedingung 5 < 10, false ist